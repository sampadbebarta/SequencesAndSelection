read -p "Enter a Number: " number
case $number in
			1)
					echo "one"
					;;
			2)
					echo "Two"
					;;
			3)
					echo "Three"
					;;
			4)
					echo "four"
					;;
			5)
					echo "five"
					;;
			6)
					echo "Six"
					;;
			7)
					echo "Seven"
					;;
			8)
					echo "Eight"
					;;
			9)
					echo "Nine"
					;;
			*)
					echo "enter the single digit number"
					;;
esac
